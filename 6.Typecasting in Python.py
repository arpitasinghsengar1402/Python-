# The conversion of one datatype into the other datatypes is known as typecasting.
# types of typecasting:
# 1.Explicit conversion: done by developer
# 2.Implicit conversion: automatically done by the python




a='5'
a=1
b='2'
b=2
print(a+b)
print(a+a)
print(int(a)+int(b))   #same output


#implicit casting
c=1.9
