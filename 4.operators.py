# operators- python has different types of operators for different operations. to create a calculator we require arithmatic operators.
# operator   || operator name   || example
#    +       ||    addition     || 15+7
#    -       ||   substraction  || 15-7
#    *       ||   multiplication|| 15*7
#    /       ||   division      || 15/7
#    %       ||   modulus       || 15%7
#   //       || floor division  || 15//7
#   **       ||  exponential    || 5**3

print(15+6)
print(15-6)
print(15*6)
print(15/6)
print(15%6)
print(15//6)
